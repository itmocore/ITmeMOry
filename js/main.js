function initCards() {
  var grid = document.getElementById('buildings-grid');
  if (!grid) return;
  BUILDINGS.forEach(function(b, i) {
    var card = document.createElement('article');
    card.className = 'building-card fade-up';
    card.setAttribute('data-id', b.id);
    if (i > 0) card.classList.add('fade-up-delay-' + Math.min(i, 4));
    card.innerHTML =
      '<div class="card-img"><div class="card-img-placeholder">' + b.name[0] + '</div><div class="card-img-year">' + b.year + '</div></div>' +
      '<div class="card-body"><div class="card-address">' + b.address + '</div><h3 class="card-name">' + b.name + '</h3>' +
      '<p class="card-desc">' + b.summary + '</p>' +
      '<div class="card-meta"><span>🏛 ' + b.style.split(',')[0] + '</span><span>📅 ИТМО с ' + b.joined + '</span></div>' +
      '<div class="card-link">Читать историю →</div></div>';
    card.addEventListener('click', function() { window.location.href = 'buildings/' + b.id + '.html'; });
    grid.appendChild(card);
  });
  observeFadeElements();
}


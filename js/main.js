function observeFadeElements() {
  var elements = document.querySelectorAll('.fade-up:not(.visible)');
  if (!('IntersectionObserver' in window)) { elements.forEach(function(el) { el.classList.add('visible'); }); return; }
  var observer = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) { if (entry.isIntersecting) { entry.target.classList.add('visible'); observer.unobserve(entry.target); } });
  }, { threshold: 0.12, rootMargin: '0px 0px -40px 0px' });
  elements.forEach(function(el) { observer.observe(el); });
}


function loadBuildingPage() {
  var pathParts = window.location.pathname.split('/');
  var fileName = pathParts[pathParts.length - 1].replace('.html', '');
  var building = null;
  for (var i = 0; i < BUILDINGS.length; i++) {
    if (BUILDINGS[i].id === fileName) { building = BUILDINGS[i]; break; }
  }
  if (!building) { document.title = 'Корпус не найден — ITMEMORY'; return; }

  document.title = building.name + ' — ITMEMORY / ИТМО';
  document.getElementById('b-tag').textContent = building.address;
  document.getElementById('b-title').textContent = building.name;
  document.getElementById('b-addr').textContent = building.address;
  document.getElementById('b-year').textContent = building.year;
  document.getElementById('b-style').textContent = building.style;
  document.getElementById('b-joined').textContent = building.joined;

  // Hero-фон из папки images (если картинка есть)
  var hero = document.querySelector('.building-hero');
  if (hero) {
    hero.style.backgroundImage = 'linear-gradient(to bottom, rgba(10,20,60,0.55), rgba(10,20,60,0.82)), url("../images/' + building.id + '.jpg")';
    hero.style.backgroundSize = 'cover';
    hero.style.backgroundPosition = 'center';
  }

  var historyEl = document.getElementById('b-history');
  building.history.split('\n\n').forEach(function(para) {
    var p = document.createElement('p');
    p.className = 'building-history-text';
    p.textContent = para.trim();
    historyEl.appendChild(p);
  });

  var factsList = document.getElementById('b-facts-list');
  building.facts.forEach(function(fact) {
    var li = document.createElement('li'); li.textContent = fact; factsList.appendChild(li);
  });

  document.getElementById('b-role').textContent = building.role;

  var sourcesList = document.getElementById('b-sources');
  building.sources.forEach(function(src) {
    var li = document.createElement('li'); li.textContent = src; sourcesList.appendChild(li);
  });

  if (typeof L !== 'undefined') {
    var mapEl = document.getElementById('building-map');
    if (mapEl) {
      var map = L.map('building-map', { center: [building.lat, building.lng], zoom: 16, zoomControl: true, scrollWheelZoom: false });
      L.tileLayer('https://{s}.basemaps.cartocdn.com/light_all/{z}/{x}/{y}{r}.png', { attribution: '© OpenStreetMap © CARTO', subdomains: 'abcd', maxZoom: 19 }).addTo(map);
      L.marker([building.lat, building.lng], { icon: L.divIcon({ className: '', html: '<div style="width:18px;height:18px;background:#1a4fd6;border:3px solid white;border-radius:50%;box-shadow:0 3px 12px rgba(26,79,214,0.5)"></div>', iconSize: [18,18], iconAnchor: [9,9] }) }).addTo(map).bindPopup(building.address).openPopup();
    }
  }

  var idx = BUILDINGS.indexOf(building);
  var prevB = BUILDINGS[idx - 1], nextB = BUILDINGS[idx + 1];
  var navEl = document.getElementById('building-nav');
  if (navEl) {
    if (prevB) { var prev = document.createElement('a'); prev.href = prevB.id + '.html'; prev.className = 'back-btn'; prev.innerHTML = '← ' + prevB.name; navEl.appendChild(prev); }
    else { navEl.appendChild(document.createElement('span')); }
    if (nextB) { var next = document.createElement('a'); next.href = nextB.id + '.html'; next.className = 'back-btn'; next.innerHTML = nextB.name + ' →'; navEl.appendChild(next); }
  }

  setTimeout(function() { document.querySelectorAll('.fade-up').forEach(function(el) { el.classList.add('visible'); }); }, 100);
}

document.addEventListener('DOMContentLoaded', loadBuildingPage);
